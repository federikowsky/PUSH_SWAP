#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	if (!lst || !new)
		return ;
	if (*lst == NULL)
	{
		*lst = new;
		return ;
	}
	if ((*lst)->next == NULL)
		(*lst)->next = new;
	else
		ft_lstadd_back(&(*lst)->next, new);
}
